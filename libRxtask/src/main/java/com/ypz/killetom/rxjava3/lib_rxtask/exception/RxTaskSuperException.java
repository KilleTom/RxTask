package com.ypz.killetom.rxjava3.lib_rxtask.exception;

public class RxTaskSuperException extends Exception {

    public RxTaskSuperException() {
        super();
    }

    public RxTaskSuperException(String message) {
        super(message);
    }

    public RxTaskSuperException(String message, Throwable cause) {
        super(message, cause);
    }

    public RxTaskSuperException(Throwable cause) {
        super(cause);
    }

}
